class Solution
{
    //Function to find the maximum profit and the number of jobs done.
    int[] JobScheduling(Job arr[], int n){
        Arrays.sort(arr, (Job a, Job b) -> b.profit - a.profit);
        int maxDeadline = -1;
        for(Job a : arr) maxDeadline = Math.max(maxDeadline, a.deadline);
        int[] vis = new int[maxDeadline];
        int count = 0;
        int ans = 0;
        
        for(Job a : arr){
            int i = a.deadline - 1;
            while(i >= 0 && vis[i] != 0) i--;
            if(i < 0) continue;
            
            vis[i] = 1;
            count++;
            ans += a.profit;
        }
        
        return new int[]{count, ans};
    }
}

